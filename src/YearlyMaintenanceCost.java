public class YearlyMaintenanceCost
{
    public static void main(String[] args)
    {
        int springCost = 100;
        int winterCost = 150;
        int summerCost = 200;
        int fallCost = 300;
        int totalCost = springCost + winterCost + fallCost + summerCost;
        System.out.println("the total yearly maintenance cost" + totalCost );
    }
}